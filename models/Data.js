const mongoose = require("mongoose");
const DataSchema = mongoose.Schema({
  userId: {
    type: String,
    required: true,
  },
  app: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("DataCollection", DataSchema);
